/**
 * 글마다 고유한 인라인 SVG 썸네일.
 * slug를 시드로 해시 → 모티프를 고르고 내부 도형을 의사난수로 변형해
 * 같은 카테고리라도 모든 글의 썸네일이 다르게 보이도록 함.
 */
const MOTIFS = ["chart", "graph", "pages", "layout", "gauge", "orbit"] as const;
type Motif = (typeof MOTIFS)[number];

function hashSeed(s: string): number {
  let h = 2166136261;
  for (let i = 0; i < s.length; i++) {
    h ^= s.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  return h >>> 0;
}

// mulberry32 의사난수 생성기 (시드 동일 → 항상 같은 결과)
function makeRng(seed: number): () => number {
  let state = seed || 1;
  return () => {
    state = (state + 0x6d2b79f5) | 0;
    let t = Math.imul(state ^ (state >>> 15), 1 | state);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

export function getPostBySlug(slug: string): BlogPost | undefined {
  return POSTS.find((p) => p.slug === slug);
}

export function Thumbnail({
  category,
  seed = "",
  large = false,
}: {
  category: string;
  seed?: string;
  large?: boolean;
}) {
  const h = hashSeed(seed || category);
  const rng = makeRng(h);
  const motif: Motif = MOTIFS[h % MOTIFS.length];
  const rand = (min: number, max: number) => min + rng() * (max - min);
  const accentIdx = Math.floor(rng() * 4);

  return (
    <div
      className={
        "relative w-full overflow-hidden rounded-[6px] border border-[#262626] bg-[#0f0f0f] " +
        (large ? "aspect-[16/7]" : "aspect-[16/10] sm:aspect-auto sm:h-[110px]")
      }
    >
      <svg
        viewBox="0 0 320 200"
        className="absolute inset-0 w-full h-full"
        preserveAspectRatio="xMidYMid slice"
        aria-hidden="true"
      >
        {motif === "chart" &&
          (() => {
            const bars = Array.from({ length: 5 }, (_, i) => {
              const ht = rand(40, 130);
              return { x: 26 + i * 30, y: 170 - ht, h: ht, accent: i === accentIdx };
            });
            const pts = Array.from({ length: 5 }, (_, i) => [180 + i * 29, rand(58, 150)]);
            return (
              <>
                {bars.map((b, i) => (
                  <rect
                    key={i}
                    x={b.x}
                    y={b.y.toFixed(1)}
                    width="20"
                    height={b.h.toFixed(1)}
                    rx="2"
                    fill={b.accent ? "#00c800" : "#1f1f1f"}
                    opacity={b.accent ? 0.85 : 1}
                  />
                ))}
                <polyline
                  points={pts.map((p) => `${p[0]},${p[1].toFixed(1)}`).join(" ")}
                  fill="none"
                  stroke="#00c800"
                  strokeWidth="2.5"
                  strokeLinecap="round"
                  strokeLinejoin="round"
                />
                {pts.map((p, i) => (
                  <circle key={i} cx={p[0]} cy={p[1].toFixed(1)} r="3.5" fill="#00c800" />
                ))}
              </>
            );
          })()}

        {motif === "graph" &&
          (() => {
            const n = 4 + Math.floor(rng() * 3);
            const nodes = Array.from({ length: n }, () => [rand(38, 282), rand(40, 160)]);
            return (
              <>
                {nodes.map((p, i) => (
                  <line key={"l" + i} x1="160" y1="100" x2={p[0].toFixed(1)} y2={p[1].toFixed(1)} stroke="#262626" strokeWidth="1.5" />
                ))}
                {nodes.map((p, i) => (
                  <circle key={"n" + i} cx={p[0].toFixed(1)} cy={p[1].toFixed(1)} r={rand(6, 10).toFixed(1)} fill="#1f1f1f" stroke="#3a3a3a" strokeWidth="1" />
                ))}
                <circle cx="160" cy="100" r="15" fill="#00c800" opacity="0.9" />
                <circle cx="160" cy="100" r="25" fill="none" stroke="#00c800" strokeWidth="1" opacity="0.3" />
              </>
            );
          })()}

        {motif === "pages" &&
          (() => {
            const rows = [92, 108, 124, 140].map((y, i) => ({ y, w: rand(58, 100), accent: i === accentIdx % 4 }));
            return (
              <>
                <rect x="96" y="44" width="128" height="120" rx="6" fill="#161616" stroke="#262626" strokeWidth="1.5" />
                <rect x="84" y="56" width="128" height="120" rx="6" fill="#1b1b1b" stroke="#3a3a3a" strokeWidth="1.5" />
                <rect x="72" y="68" width="128" height="120" rx="6" fill="#0f0f0f" stroke="#00c800" strokeWidth="1.5" />
                {rows.map((r, i) => (
                  <rect key={i} x="88" y={r.y} width={r.w.toFixed(1)} height="7" rx="3.5" fill={r.accent ? "#00c800" : "#2a2a2a"} opacity={r.accent ? 0.85 : 1} />
                ))}
              </>
            );
          })()}

        {motif === "layout" &&
          (() => {
            const headW = rand(96, 150);
            const l1 = rand(118, 172);
            const l2 = rand(88, 150);
            return (
              <>
                <rect x="48" y="36" width="224" height="128" rx="8" fill="#0f0f0f" stroke="#262626" strokeWidth="1.5" />
                <rect x="48" y="36" width="224" height="26" rx="8" fill="#1a1a1a" />
                <circle cx="64" cy="49" r="3" fill="#00c800" />
                <rect x="80" y="46" width="60" height="6" rx="3" fill="#2a2a2a" />
                <rect x="64" y="78" width={headW.toFixed(1)} height="10" rx="3" fill="#00c800" opacity="0.85" />
                <rect x="64" y="98" width={l1.toFixed(1)} height="6" rx="3" fill="#2a2a2a" />
                <rect x="64" y="112" width={l2.toFixed(1)} height="6" rx="3" fill="#2a2a2a" />
                <rect x="64" y="132" width="84" height="16" rx="4" fill="#1f1f1f" stroke="#3a3a3a" strokeWidth="1" />
                <rect x="212" y="78" width="44" height="70" rx="4" fill="#161616" stroke="#262626" strokeWidth="1" />
              </>
            );
          })()}

        {motif === "gauge" &&
          (() => {
            const frac = rand(0.32, 0.86);
            const ang = ((180 - frac * 180) * Math.PI) / 180;
            const ex = 160 + 90 * Math.cos(ang);
            const ey = 150 - 90 * Math.sin(ang);
            const nx = 160 + 64 * Math.cos(ang);
            const ny = 150 - 64 * Math.sin(ang);
            return (
              <>
                <path d="M70 150 A90 90 0 0 1 250 150" fill="none" stroke="#262626" strokeWidth="14" strokeLinecap="round" />
                <path d={`M70 150 A90 90 0 0 1 ${ex.toFixed(1)} ${ey.toFixed(1)}`} fill="none" stroke="#00c800" strokeWidth="14" strokeLinecap="round" opacity="0.9" />
                <line x1="160" y1="150" x2={nx.toFixed(1)} y2={ny.toFixed(1)} stroke="#f0f0f0" strokeWidth="3" strokeLinecap="round" />
                <circle cx="160" cy="150" r="7" fill="#0f0f0f" stroke="#f0f0f0" strokeWidth="2.5" />
              </>
            );
          })()}

        {motif === "orbit" &&
          (() => {
            const dots = Array.from({ length: 3 + Math.floor(rng() * 3) }, () => {
              const r = rand(38, 82);
              const a = rng() * Math.PI * 2;
              return [160 + r * Math.cos(a), 100 + r * Math.sin(a)];
            });
            return (
              <>
                <circle cx="160" cy="100" r="40" fill="none" stroke="#262626" strokeWidth="1.5" />
                <circle cx="160" cy="100" r="62" fill="none" stroke="#1f1f1f" strokeWidth="1.5" />
                <circle cx="160" cy="100" r="82" fill="none" stroke="#151515" strokeWidth="1.5" />
                <circle cx="160" cy="100" r="10" fill="#00c800" opacity="0.9" />
                {dots.map((p, i) => (
                  <circle key={i} cx={p[0].toFixed(1)} cy={p[1].toFixed(1)} r="4" fill={i === 0 ? "#00c800" : "#3a3a3a"} />
                ))}
              </>
            );
          })()}
      </svg>
    </div>
  );
}

